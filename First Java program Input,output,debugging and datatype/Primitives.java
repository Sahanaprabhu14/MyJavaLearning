public class Primitives {
	public static void main(String[] args) {
		int rollno=36; //integer values (4 bytes)
		char letter='S'; //character values
		float marks=9.12f; //decimal point values (4 bytes) (f- all the decimal values are by default of type double, to store in float we have to add f)
		double LargeDecimalNumbers= 33557244.9762327936982; //large decimal values (8 bytes)
		long LargeIntegers=92733864924751L; //large integers (8 bytes)
		boolean check=true; //(or false)- boolean values either true or false

		//String is not primitive datatype(Primitive-anything that you cannot break further)
		String name= "Sahana";
	}
}